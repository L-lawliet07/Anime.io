const mongoose = require('mongoose');


///////////////////////////////////////////////////////////
// Creating a crew schema
const crewSchema = new mongoose.Schema({

    name: {
        type: String,
        required: [true, 'Crew name is required'],
        unique: true
    },
    image: {
        type: String,
        default: 'default.png'
    },
    members: [String]
});
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Creating crew model
const crew = mongoose.model('Crew', crewSchema);
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// exposing crew model
module.exports = crew;

const mongoose = require('mongoose');


///////////////////////////////////////////////////////////
// Creating a crewMessage schema
const MessageSchema = new mongoose.Schema({

    sender: {
        type: String,
    },
    receiver: {
        type: String,
    },
    body: {
        type: String,
        minlength: 1
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Creating crew model
const Message = mongoose.model('Message', MessageSchema);
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// exposing crew model
module.exports = Message;

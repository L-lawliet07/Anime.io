const mongoose = require('mongoose');


///////////////////////////////////////////////////////////
// Creating a crewMessage schema
const crewMessageSchema = new mongoose.Schema({

    sender: {
        type: String,
    },
    crew: {
        type: String
    },
    body: {
        type: String,
        minlength: 1
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Creating crew model
const crewMessage = mongoose.model('CrewMessage', crewMessageSchema);
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// exposing crew model
module.exports = crewMessage;

const globalErrorHandler = require('./controllers/errorController');

const AppError = require('./utils/appError');

const path = require('path');

const express = require('express');

const morgan = require('morgan');

const cookieParser = require('cookie-parser');

const compression = require('compression');

///////////////////////////////////////////////////////////
// Routes
const userRoute = require('./routes/userRoute');
const viewRoute = require('./routes/viewRoute');
const crewRoute = require('./routes/crewRoute');
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Starting express app
const app = express();
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// setting up view engine and view path
app.set('view engine', 'ejs');
app.set('views', path.join(__dirname, 'views'));
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Running morgan for only development
if (process.env.NODE_ENV === 'development') {
    app.use(morgan('dev'));
}
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// For parsing json request body
app.use(express.json());
app.use(cookieParser());
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Serving static files
app.use(express.static(path.join(__dirname, 'public')));
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Compression middleware to compress text response
app.use(compression());
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// middleware to set requestTime in request object
app.use((req, res, next) => {
    req.requestTime = new Date().toISOString();
    next();
});
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Setting up Routes
app.use('/', viewRoute);
app.use('/crew', crewRoute);
app.use('/user', userRoute);
app.all('*', (req, res, next) => {
    const err = new AppError(`Page not found`, 404);
    next(err);
});
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// adding error handling middleware
app.use(globalErrorHandler);
///////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////
// Exposing express app to other modules 
module.exports = app;
///////////////////////////////////////////////////////////